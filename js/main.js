var $hide_boxes = $('.show_elems');

window.addEventListener('scroll', function(){

    $hide_boxes.each(function(){
        let offset = $(this)[0].getBoundingClientRect().bottom - document.documentElement.clientHeight;
        let elems = $(this).find('.no_elem');
        if (offset < 0)
        {
            let i = 0;
            for(let item of elems)
            {
                setTimeout(function() {
                       item.classList.remove('hide_this');
                }, i*500);
                i++;
            }
        }
        else
        {
            elems.addClass('hide_this');
        }
    });

});





